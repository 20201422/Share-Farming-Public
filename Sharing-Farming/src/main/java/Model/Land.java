package Model;

public class Land {
    private String lno;
    private String lna;
    private int lst;
    private float lpr;
    private int lar;
    private String lpo;
    private String Lmg;

    public String getLno() {
        return lno;
    }

    public void setLno(String lno) {
        this.lno = lno;
    }

    public String getLna() {
        return lna;
    }

    public void setLna(String lna) {
        this.lna = lna;
    }

    public int getLst() {
        return lst;
    }

    public void setLst(int lst) {
        this.lst = lst;
    }

    public float getLpr() {
        return lpr;
    }

    public void setLpr(float lpr) {
        this.lpr = lpr;
    }

    public int getLar() {
        return lar;
    }

    public void setLar(int lar) {
        this.lar = lar;
    }

    public String getLpo() {
        return lpo;
    }

    public void setLpo(String lpo) {
        this.lpo = lpo;
    }

    public String getLmg() {
        return Lmg;
    }

    public void setLmg(String lmg) {
        Lmg = lmg;
    }

}
